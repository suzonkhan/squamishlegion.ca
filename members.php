          		<h2>A massege from the President</h2>
				<p1>Once again we did not have enough people to make up a Quorum, therefore we could not finish our business, so now because the  next General Meeting is not until September we will now have to have an Executive Meeting to finish our business and present it at the next General Meeting and vote to accept or reject the motions that have been put before you, this is not a good way to conduct business, we from the President on downwards are all equals and your vote as members is essential to the Operation of the Legion, but you can’t do it if you don’t attend meetings and accept or decline motions that have been put forward or put your own motions out on the floor, these meetings generally take about 45 minutes and are held on the last Wednesday of the month at 7:00 pm which I think is a convenient time, I do not expect everyone to be at every meeting but I do expect enough to make a Quorum which by policy the Quorum for a General Meeting cannot be less than three (3) more then the total number of the Executive Committee which is seven (7) this is regardless of all seven (7) being at the meeting, the number still stands so that means we need ten (10) Members in attendance. I don’t think that’s asking too much considering we have over 160 Members, I realize some of the Members no longer live in Squamish and I know some are not in good enough health to attend. If we continue to be unable to achieve a Quorum at every General Meeting then the Legion will be in trouble because without enough attendees we will be unable to carry out Legion business as it should be carried out, come September which is the next General Meeting I expect to meet the Quorum at least, the ball is in your court now, so it’s up to you. I personally am getting fed up with feeling like I am banging my head against a brick wall and I am not going to carry on doing it much longer, as far as I am concerned if we can’t do business the way it should be done then we can’t do it at all, as Legion Members 